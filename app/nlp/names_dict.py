# ═══════════════════════════════════════════════════════════
#  ИНДЕКСЫ для быстрого lookup
# ═══════════════════════════════════════════════════════════

# nominative → NameEntry
NOM_INDEX: dict[str, NameEntry] = {}

# genitive → list[NameEntry]  (list because gen can map to M and F)
GEN_INDEX: dict[str, list[NameEntry]] = {}

# any form → list[NameEntry]
ANY_FORM_INDEX: dict[str, list[NameEntry]] = {}

# Ambiguous forms: where gen(male) == nom(female)
# e.g. "Александра" → {male: NameEntry(Александр), female: NameEntry(Александра)}
AMBIGUOUS: dict[str, dict[str, NameEntry]] = {}

# All known nominative forms
ALL_NOMINATIVES: set[str] = set()
ALL_GENITIVES: set[str] = set()


def _build_indexes():
    """Build all lookup indexes from _RAW_NAMES."""
    for nom, gen, gender in _RAW_NAMES:
        entry = NameEntry(nominative=nom, genitive=gen, gender=gender)

        # Nominative index
        NOM_INDEX[nom] = entry
        ALL_NOMINATIVES.add(nom)

        # Genitive index (can have multiple entries for same form)
        if gen not in GEN_INDEX:
            GEN_INDEX[gen] = []
        GEN_INDEX[gen].append(entry)
        ALL_GENITIVES.add(gen)

        # Any-form index
        for form in (nom, gen):
            if form not in ANY_FORM_INDEX:
                ANY_FORM_INDEX[form] = []
            if entry not in ANY_FORM_INDEX[form]:
                ANY_FORM_INDEX[form].append(entry)

    # Build ambiguous index
    # A form is ambiguous if it's BOTH a genitive of a male name
    # AND a nominative of a female name
    for form, entries in ANY_FORM_INDEX.items():
        male_gen = [e for e in entries if e.gender == "м" and e.genitive == form]
        female_nom = [e for e in entries if e.gender == "ж" and e.nominative == form]

        if male_gen and female_nom:
            AMBIGUOUS[form] = {
                "м": male_gen[0],
                "ж": female_nom[0],
            }


_build_indexes()


# ═══════════════════════════════════════════════════════════
#  LOOKUP FUNCTIONS
# ═══════════════════════════════════════════════════════════

def lookup_nominative(form: str) -> NameEntry | None:
    """Lookup by nominative form. Returns exact match or None."""
    return NOM_INDEX.get(form)


def lookup_genitive(form: str) -> list[NameEntry]:
    """Lookup by genitive form. May return multiple entries."""
    return GEN_INDEX.get(form, [])


def lookup_any(form: str) -> list[NameEntry]:
    """Lookup by any form (nominative or genitive)."""
    return ANY_FORM_INDEX.get(form, [])


def is_ambiguous(form: str) -> bool:
    """Check if this form is ambiguous (gen_male == nom_female)."""
    return form in AMBIGUOUS


def get_ambiguous_pair(form: str) -> dict[str, NameEntry] | None:
    """Get the ambiguous pair: {"м": ..., "ж": ...}."""
    return AMBIGUOUS.get(form)


def get_gender_hint_from_prefix(prefix: str | None) -> str | None:
    """
    Some prefixes hint at gender.

    болящей, заблудшей, путешествующей, непраздной → ж
    болящего, заблудшего, путешествующего, воина → м
    """
    if not prefix:
        return None
    fem = {"болящая", "заблудшая", "путешествующая", "непраздная", "отроковица"}
    masc = {"болящий", "заблудший", "путешествующий", "воин", "отрок", "младенец"}
    if prefix in fem:
        return "ж"
    if prefix in masc:
        return "м"
    return None
