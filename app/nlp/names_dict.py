"""
Словарь церковных (крестильных) имён.

Каждое имя хранится как NameEntry:
  - nominative:  именительный падеж (каноническая форма)
  - genitive:    родительный падеж (кого? — форма в записках)
  - gender:      "м" | "ж"

КЛЮЧЕВАЯ ПРОБЛЕМА: "Александра"
  - Родительный от мужского "Александр" → "Александра"
  - Именительный женского "Александра"  → "Александра"
  Одинаковая форма! Нужен контекст для разрешения.

ПРАВИЛО:
  Записки пишутся в родительном падеже (О здравии КОГО?).
  Поэтому "Александра" по умолчанию = род.п. мужского "Александр",
  ЕСЛИ контекст не указывает иное.

Словарь индексирован по ВСЕМ формам для быстрого lookup.
"""

from dataclasses import dataclass, field


@dataclass(frozen=True)
class NameEntry:
    """Одно имя в словаре."""
    nominative: str       # Именительный: Александр
    genitive: str         # Родительный:  Александра
    gender: str           # "м" | "ж"

    def __repr__(self):
        return f"{self.nominative}({self.gender}) [gen: {self.genitive}]"


